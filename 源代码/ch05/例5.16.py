import numpy as np
A = np.array([[8.5,9], [4.5,4], [9,9.5]])  #不同商店三种水果的价格
B = np.array([[5,10,3],[4,5,5]])       #不同人员需要水果的数量
C = np.array([[1000,500],[2000,1000]])  #不同城镇不同人员的数目
D = B.dot(A)                     #每个商店每个人购买水果的费用
E = C.dot(B)                      #每个城镇每种水果的购买量
print("每个商店每个人购买水果的费用为：\n",D)
print("每个城镇每种水果的购买量为：\n",E)
